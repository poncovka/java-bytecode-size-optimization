package jbyco.basic_examples;

public class ConstantNumbers {

    int ints() {
        int a = 0;
        a = 10;
        a = 100;
        a = 255;
        a = 1000;
        a = 10000000;
        return a;
    }

    long longs() {
        long a = 0;
        a = 10;
        a = 100;
        a = 255;
        a = 1000;
        a = 10000000;
        return a;
    }

    double doubles() {
        double a = 0;
        a = -1;
        a = 10;
        a = 100;
        a = 255;
        a = 100000;
        a = 1000000;
        a = -1.5;
        a = 10.5;
        a = 100.5;
        a = 255.5;
        a = 100000.5;
        a = 1000000.5;
        a = Double.NaN;
        return a;
    }

    float floats() {
        float a = 0;
        a = -1;
        a = 10;
        a = 100;
        a = 255;
        a = 100000;
        a = 1000000;
        a = -1.5f;
        a = 10.5f;
        a = 100.5f;
        a = 255.5f;
        a = 100000.5f;
        a = 1000000.5f;
        a = Float.NaN;
        return a;
    }

}
